/**
 * op_add - addition
 * @a: argument 1
 * @b: argument 2
 *
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 * @a: argument 1
 * @b: argument 2
 *
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: argument 1
 * @b: argument 2
 *
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: argument 1
 * @b: argument 2
 *
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modules
 * @a: argument 1
 * @b: argument 2
 *
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
